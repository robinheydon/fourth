pub const Event = union(EventType) {
    enter: EnterEvent,
    focus: FocusEvent,
    key_down: KeyEvent,
    key_up: KeyEvent,
    mouse_move: MoveEvent,
    mouse_down: MouseEvent,
    mouse_double_click: MouseEvent,
    mouse_up: MouseEvent,
    mouse_wheel: WheelEvent,
    text: TextEvent,
    leave: LeaveEvent,
    quit: QuitEvent,
    resize: ResizeEvent,
    unfocus: UnfocusEvent,
    user: UserEvent,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const EventType = enum {
    enter,
    focus,
    key_down,
    key_up,
    mouse_move,
    mouse_down,
    mouse_double_click,
    mouse_up,
    mouse_wheel,
    text,
    leave,
    quit,
    resize,
    unfocus,
    user,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const QuitEvent = struct {};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const ResizeEvent = struct {
    width: f32,
    height: f32,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const KeyEvent = struct {
    key: Key,
    scan_code: u32, // arbitrary
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const MoveEvent = struct {
    buttons: MouseButtons = .{},
    x: f32,
    y: f32,
};

pub const MouseButtons = packed struct {
    left: bool = false,
    middle: bool = false,
    right: bool = false,
    x1: bool = false,
    x2: bool = false,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const MouseEvent = struct {
    button: MouseButton,
    x: f32,
    y: f32,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const MouseButton = enum(u8) {
    none,
    left,
    middle,
    right,
    x1,
    x2,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const WheelEvent = struct {
    dx: f32 = 0,
    dy: f32 = 0,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const TextEvent = struct {
    cp: u32,
};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const EnterEvent = struct {};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const LeaveEvent = struct {};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const FocusEvent = struct {};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const UnfocusEvent = struct {};

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub const UserEvent = struct {
    data0: usize = 0,
    data1: usize = 0,
    data2: usize = 0,
    data3: usize = 0,
};

