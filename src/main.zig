const std = @import("std");

const ng = @import("ng");

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

var running = true;

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub fn main() !void {
    try ng.init(.{
        .video = true,
        .audio = true,
    });
    defer ng.deinit();

    const window = try ng.create_window(.{
        .name = "Fourth",
        .width = 1920,
        .height = 1080,
        .resizable = true,
    });
    defer window.close();

    const shader = try ng.create_shader(@import("triangle_shader.zig"));
    defer shader.delete();

    const buffer = try ng.create_buffer (.{});
    defer buffer.delete ();

    std.debug.print("{}\n", .{shader});

    while (running) {
        while (ng.poll_event()) |event| {
            switch (event) {
                .quit => {
                    running = false;
                },
                .key_down => |key_event| {
                    if (key_event.key == 9) {
                        running = false;
                    }
                },
                .resize => {},
                .enter => {},
                .leave => {},
                .focus => {},
                .unfocus => {},
                else => {
                    std.debug.print("{}\n", .{event});
                },
            }
        }

        const command_buffer = try window.acquire_command_buffer();
        const swapchain_texture = try command_buffer.acquire_swapchain_texture();
        const render_pass = try command_buffer.begin_render_pass(.{
            .texture = swapchain_texture,
            .clear_color = .black,
            .load = .clear,
            .store = .store,
        });
        render_pass.end();
        try command_buffer.submit();
    }
}

